# Conversion of 'Binary Number' to 'Decimal Number'
def binary_to_decimal(binary_number):
    # Here we are using Python built-in int() function which returns an integer from a string or number. If no argument is passed then it returns 0 bypasses the base 2 to find decimal of binary number.
    decimal_number = int(binary_number,2)
    
    return (decimal_number)

